package com.eloisapaz.cloud.twitter;

public class TwitterData {
    private String user;
    private int tweets;

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public int getTweets() {
        return tweets;
    }

    public void setTweets(int tweets) {
        this.tweets = tweets;
    }
}